function getRandomArbitrary(min: number, max: number) {
    return Math.floor(Math.random() * (max + min + 1)) + min;
}

const makeFillerFile = (count: number) => {
    const identities = [];
    const chiefs = [];

    for(let i = 0; i <= count; i++) {
        const firstName = names[getRandomArbitrary(0, names.length)];
        const lastName = surNames[getRandomArbitrary(0, surNames.length)];

        const user_name = laravelSlug(`${firstName?.[0] ?? ''}${lastName}`, {});

        if (i === 0) {
            chiefs.push(user_name);
        }

        identities.push({
            identity: {
                user_name,
                name: `${firstName} ${lastName}`,
                birthday: `${getRandomArbitrary(0, 28)} ${shortMounthsMmm[getRandomArbitrary(0, shortMounthsMmm.length)]}.`,
                job_title: jobTitles[getRandomArbitrary(0, jobTitles.length)],
                division1: departments[getRandomArbitrary(0, departments.length)],
                division2: subDivisions[getRandomArbitrary(0, subDivisions.length)],
                division3: subDivisions2[getRandomArbitrary(0, subDivisions2.length*2)],
                chief: chiefs[getRandomArbitrary(0, chiefs.length)],
                entry_date: "2018-04-13",
                vacation_left: getRandomArbitrary(1, 45),
                vacation_dates: "05.05.2021 - 04.02.2024"
            }
        });
    }
};

